function enExpiry(ms: number): string {
  if (!ms) return 'never';
  const d = Math.ceil((ms - Date.now()) / 86400e3);
  if (d <= 0) {
    const h = Math.ceil((ms - Date.now()) / 3600e3);
    return h <= 0 ? 'expired' : h + (h === 1 ? ' hour' : ' hours');
  }
  return d === 1 ? '1 day' : d + ' days';
}

