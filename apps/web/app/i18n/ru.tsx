function ruFiles(n: number): string {
  const a =
    [2, 0, 1, 1, 1, 2][
      n % 100 > 4 && n % 100 < 20 ? 2 : [2, 0, 1, 1, 1, 2][Math.min(n % 10, 5)]!
    ]!;
  return ['файл', 'файла', 'файлов'][a]!;
}

function ruChars(n: number): string {
  const m = n % 100;
  if (m > 4 && m < 20) return 'символов';
  const d = n % 10;
  if (d === 1) return 'символ';
  if (d >= 2 && d <= 4) return 'символа';
  return 'символов';
}

function ruExpiry(ms: number): string {
  if (!ms) return 'бессрочно';
  const d = Math.ceil((ms - Date.now()) / 86400e3);
  if (d <= 0) {
    const h = Math.ceil((ms - Date.now()) / 3600e3);
    return h <= 0 ? 'истекла' : h + ' ч';
  }
  return d === 1 ? '1 день' : d + ' дн.';
}

