function zhExpiry(ms: number): string {
  if (!ms) return '永久';
  const d = Math.ceil((ms - Date.now()) / 86400e3);
  if (d <= 0) {
    const h = Math.ceil((ms - Date.now()) / 3600e3);
    return h <= 0 ? '已過期' : h + ' 小時';
  }
  return d + ' 天';
}

